// دریافت اولین عدد از کاربر
const number1 = parseFloat(prompt("لطفاً اولین عدد را وارد کنید:"));

// دریافت دومین عدد از کاربر
const number2 = parseFloat(prompt("لطفاً دومین عدد را وارد کنید:"));

// ضرب دو عدد
const result = number1 * number2;

// نمایش نتیجه
alert(`نتیجه ضرب ${number1} و ${number2} برابر است با: ${result}`);